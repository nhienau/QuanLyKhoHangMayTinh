    private void btnHoatDongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHoatDongActionPerformed
        // TODO add your handling code here:
        int row = tbSanPham.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm muốn kinh doanh!");
            return ;
        }
        String tenSP = tbSanPham.getValueAt(row, 1).toString();
        SanPhamDTO SP = SanPhamDAO.getInstance().selectDeletedProductByName(tenSP);
        if(loaiSanPhamDAO.getInstance().getNameOfType(SP.getMaLoaiSanPham()).equals("")){
            JOptionPane.showMessageDialog(this, "Thương hiệu của sản phẩm này không tồn tại!");
            return ;
        }
        JOptionPane.showMessageDialog(this,spBUS.reuseProduct(SP.getMaSanPham()) );
        ArrayList<SanPhamDTO> arr = SanPhamDAO.getInstance().getUnuselistProduct();
        loadTable(arr);
    }//GEN-LAST:event_btnHoatDongActionPerformed

    private void cbbActiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbActiveActionPerformed
        // TODO add your handling code here:
        if(cbbActive.getSelectedItem() == "Hoạt động"){
            ArrayList<SanPhamDTO> arr = SanPhamDAO.getInstance().getlistProduct();
            loadTable(arr);
            btnHoatDong.setEnabled(false);
            btnNgungHoatDong.setEnabled(true);
        } else {
            ArrayList<SanPhamDTO> arr = SanPhamDAO.getInstance().getUnuselistProduct();
            loadTable(arr);
            btnNgungHoatDong.setEnabled(false);
            btnHoatDong.setEnabled(true);
        }
    }//GEN-LAST:event_cbbActiveActionPerformed

    private void btnNgungHoatDongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNgungHoatDongActionPerformed
        // TODO add your handling code here:
        int row = tbSanPham.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm muốn ngừng kinh doanh!");
            return ;
        }
        String tenSP = tbSanPham.getValueAt(row, 1).toString();
        SanPhamDTO SP = SanPhamDAO.getInstance().selectProductByName(tenSP);
        JOptionPane.showMessageDialog(this,spBUS.deleteProduct(SP.getMaSanPham()) );
        ArrayList<SanPhamDTO> arr = SanPhamDAO.getInstance().getlistProduct();
        loadTable(arr);
    }//GEN-LAST:event_btnNgungHoatDongActionPerformed

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        String content = txtSearch.getText();
        if(cbbActive.getSelectedItem() == "Hoạt động"){
            ArrayList<SanPhamDTO> result = SearchProduct.getInstance().searchTenSanPham(content);
            loadTable(result);
        } else {
            ArrayList<SanPhamDTO> result = SearchProduct.getInstance().searchDeletedTenSanPham(content);
            loadTable(result);
        }
        
    }//GEN-LAST:event_txtSearchKeyReleased

