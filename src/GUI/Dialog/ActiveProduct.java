    public void loadTable(ArrayList<SanPhamDTO> arr){
        modelTable.setRowCount(0);
        for(int i = 0; i< arr.size(); i++){
                SanPhamDTO spDTO = arr.get(i);
                Object[] row ={spDTO.getMaSanPham(),spDTO.getTenSanPham()};
                modelTable.addRow(row); 
        }
        for(int i = 0; i < tbSanPham.getColumnCount(); i++){
            
            tbSanPham.getColumnModel().getColumn(i).setCellRenderer(renderer);
        }
        
        
    }
