    public void loadDataToTableProduct() {
        try {
            int tt = 0;
            DefaultTableModel tblCTPhieumd = (DefaultTableModel) tblChiTietPhieu.getModel();
            tblCTPhieumd.setRowCount(0);
            for (var i : ChiTietPhieuXuatDAO.getInstance().selectAll()) {

                if (PhieuXuatGUI.CTPhieuXuat.get(0).getMaPhieuXuat() == i.getMaPhieuXuat()) {

                    labelMaPhieu.setText(String.valueOf(i.getMaPhieuXuat()));

                    tblCTPhieumd.addRow(new Object[]{
                        i.getMaSanPham(),
                        SanPhamDAO.getInstance().getNameByID(i.getMaSanPham()),
                        i.getSoLuong(),
                        formatter.format(i.getDonGia()) + "đ",
                        formatter.format((i.getDonGia()) * (i.getSoLuong())) + "đ"

                    });
                    labelTongTien.setText(formatter.format(tt += (i.getDonGia()) * (i.getSoLuong())) + "đ");
                }
            }

        } catch (Exception e) {
        }

    }

