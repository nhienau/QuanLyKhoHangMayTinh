    public void loadCbbKho(int maSP , int maNCC){
        modelCbbKho.removeAllElements();
        if(tbSanPham.getSelectedRow() != -1 && cbbNhaCungCap.getSelectedItem() != null){
            ArrayList<ChiTietTonKhoDTO> khoList = tonKhoDAO.getInstance().getListOfWareHouse(maSP, maNCC);
            ArrayList<ChiTietTonKhoDTO> kho = new ArrayList<>();
                for(int i =0 ;i< khoList.size() ; i++){
                    ChiTietTonKhoDTO cttk = khoList.get(i);
                    String tenKho = khoDAO.getInstance().getWareHouseByID(cttk.getMaKho());
                    if(!kho.contains(cttk)){
                        kho.add(cttk);
                        modelCbbKho.addElement(tenKho);
                    }
                    
                }
        
        }
       
    }
    public void loadCbbNhaCungCap(int maSP){
        modelCbbNcc.removeAllElements();
        int row = tbSanPham.getSelectedRow();
        ArrayList<ChiTietCungCapDTO> nccList = ChiTietCungCapDAO.getInstance().findNhaCungCapByMaSanPham(maSP);
        for(int i = 0 ; i < nccList.size() ; i++){
            ChiTietCungCapDTO ctcc = nccList.get(i);
            int maNCC = ctcc.getMaNhaCungCap();
            NhaCungCapDTO NCC = NhaCungCapDAO.getInstance().getByID(maNCC);
            modelCbbNcc.addElement(NCC.getTenNhaCungCap());
            int donGia = ctcc.getGiaNhap();
            txtDonGia.setText(String.valueOf(formatter.format(donGia)));
        }
    }
    public void loadTable(){
        ArrayList<SanPhamDTO> spList = SanPhamDAO.getInstance().getlistProduct();
        int stt = 0;
        for(int i = 0 ; i < spList.size() ; i++){
            SanPhamDTO sp = spList.get(i);
            stt ++;
            String tenSP = sp.getTenSanPham();
            Object[] row = {stt, tenSP};
            modelTable.addRow(row);
        }
    }
