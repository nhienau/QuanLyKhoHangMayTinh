    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        // TODO add your handling code here:
        if(tbSanPham.getSelectedRow() == -1 || cbbNhaCungCap.getSelectedItem() == null || cbbKho.getSelectedItem() == null || txtSoLuongPhanPhoi.getText().equals("")){
            JOptionPane.showConfirmDialog(this, "Vui lòng nhập đầy đủ thông tin để xác nhận!");
            return ;
        }
        int soLuongTonKho = Integer.parseInt(txtTonKho.getText().trim());
        int soLuongPP = Integer.parseInt(txtSoLuongPhanPhoi.getText().trim());
        if(soLuongPP > soLuongTonKho ){
            JOptionPane.showMessageDialog(this, "Số lượng tồn kho không đủ!");
            return ;
        }
        String tenSP = tbSanPham.getValueAt( tbSanPham.getSelectedRow(), 1).toString();
        SanPhamDTO sp = SanPhamDAO.getInstance().selectProductByName(tenSP);
        int masp = sp.getMaSanPham() ; 
        String nameNCC = cbbNhaCungCap.getSelectedItem().toString();
        NhaCungCapDTO ncc = NhaCungCapDAO.getInstance().getNCCByName(nameNCC);
        int maNCC = ncc.getMaNhaCungCap();
        
        String tenKho = cbbKho.getSelectedItem().toString() ;
        khoDTO kho = khoDAO.getInstance().getWareHouseByName(tenKho);
        int maKho = kho.getMaKho();
        
        ArrayList<ChiTietTonKhoDTO> arr = tonKhoDAO.getInstance().getListOfWareHouse(masp, maNCC);
        
        for(int i = 0; i< arr.size() ; i++){
            boolean answer = false;
            ChiTietTonKhoDTO cttk = arr.get(i);
            if(cttk.getMaKho() == maKho && cttk.getMaNhaCungCap() == maNCC && cttk.getMaSanPham() == masp){
                int soLuongUpdate = cttk.getSoLuongTonKho() - soLuongPP;
                if(tonKhoDAO.getInstance().updateSoLuong(maKho, masp, maNCC, soLuongUpdate)){
                    JOptionPane.showMessageDialog(this, "Phân phối sản phẩm thành công!");
                    answer = true ;
                } else {
                        JOptionPane.showMessageDialog(this, "Phân phối sản phẩm thất bại!");
                }
                
            }
            if(answer == true)
                return ;
        }
        

        
    }//GEN-LAST:event_btnXacNhanActionPerformed

